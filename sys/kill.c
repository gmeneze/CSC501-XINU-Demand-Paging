/*------------------------------------------------------------------------
 * kill  --  kill a process and remove it from the system
 *------------------------------------------------------------------------
 */
SYSCALL kill(int pid)
{
	STATWORD ps;    
	struct	pentry	*pptr;		/* points to proc. table for pid*/
	int	dev;

	disable(ps);
        clean_up_process(pid);
	if (isbadpid(pid) || (pptr= &proctab[pid])->pstate==PRFREE) {
		restore(ps);
		return(SYSERR);
	}

	if (--numproc == 0)
		xdone();

	dev = pptr->pdevs[0];
	if (! isbaddev(dev) )
		close(dev);
	dev = pptr->pdevs[1];
	if (! isbaddev(dev) )
		close(dev);
	dev = pptr->ppagedev;
	if (! isbaddev(dev) )
		close(dev);
	
	send(pptr->pnxtkin, pid);

	freestk(pptr->pbase, pptr->pstklen);
	switch (pptr->pstate) {

	case PRCURR:	pptr->pstate = PRFREE;	/* suicide */
			resched();

	case PRWAIT:	semaph[pptr->psem].semcnt++;

	case PRREADY:	dequeue(pid);
			pptr->pstate = PRFREE;
			break;

	case PRSLEEP:
	case PRTRECV:	unsleep(pid);
						/* fall through	*/
	default:	pptr->pstate = PRFREE;
	}
	restore(ps);
	return(OK);
}


SYSCALL clean_up_process(int pid)
{

  int frame_index;
  int bs_index;

  write_back_dirty_frm(pid, -1); // write back all frames to BS

  for(bs_index = 0; bs_index <= MAX_ID ; bs_index++)
  {

     if( proctab[pid].bs_list[bs_index].bs_status == BSM_MAPPED )
     {
         unmap_bs(bs_index,pid);
     }

  }
  
  for(frame_index = 0; frame_index < NFRAMES; frame_index++)
  {

      if( frm_tab[frame_index].fr_pid == currpid )
      {
          free_frm(frame_index);
      }

  }

  return OK;

}
